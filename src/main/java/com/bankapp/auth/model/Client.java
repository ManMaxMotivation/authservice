    @Column(nullable = false)
    private String password; // –ü–∞—Ä–æ–ª—å –∫–ª–∏–µ–Ω—Ç–∞, —Ö—Ä–∞–Ω–∏—Ç—Å—è –≤ —Å—Ç–æ–ª–±—Ü–µ `password` (–≤ —Ä–µ–∞–ª—å–Ω–æ–º –ø—Ä–æ–µ–∫—Ç–µ –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –∑–∞—Ö–µ—à–∏—Ä–æ–≤–∞–Ω)

