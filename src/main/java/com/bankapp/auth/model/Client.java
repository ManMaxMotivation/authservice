    // –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä –±–µ–∑ –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤
    // –ù–µ–æ–±—Ö–æ–¥–∏–º –¥–ª—è JPA, —Ç–∞–∫ –∫–∞–∫ Hibernate –∏—Å–ø–æ–ª—å–∑—É–µ—Ç —Ä–µ—Ñ–ª–µ–∫—Å–∏—é –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –æ–±—ä–µ–∫—Ç–æ–≤
